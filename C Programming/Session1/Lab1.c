#include <stdio.h>

int main()
{
    int x;
    int y;
    printf("Enter x: \n");
    scanf(" %d",&x);
    printf("Enter y: \n");
    scanf(" %d",&y);
    printf("x+y =  %d \n", x+y);
    printf("x-y =  %d \n", x-y);
    printf("x*y =  %d \n", x*y);
    printf("x/y =  %d \n", x/y);
    printf("x mod y =  %d \n", x%y);
}